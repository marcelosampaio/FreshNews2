import Foundation
import UIKit

public extension UIColor {
    
    class var applicationTintColor: UIColor {
        get {
            return UIColor(red: 33/255, green: 150/255, blue: 243/255, alpha: 1)
        }
    }
    
    class var superLightGrayColor: UIColor {
        get {
            return UIColor(red: 220/255, green: 220/255, blue: 220/255, alpha: 1)
        }
    }
    
    class var headerDetailLogoBorderColor: UIColor {
        get {
            return UIColor(red: 142/255, green: 142/255, blue: 147/255, alpha: 1)
        }
    }
    
    
}
